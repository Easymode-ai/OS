void pic_remap(void)
{
	write_port(0x20, 0x11);
	write_port(0xA0, 0x11);
	write_port(0x21, 0x20);
	write_port(0xA1, 0x28);
	write_port(0x21, 0x04);
	write_port(0xA1, 0x02);
	write_port(0x21, 0x01);
	write_port(0xA1, 0x01);
	write_port(0x21, 0x0);
	write_port(0xA1, 0x0);
}